#リスト内包表記
print([i**2 for i in [1,2,3]])
"""
[出力]
[1, 4, 9]
"""

#リスト内包表記(条件付き：iが2のものだけ)
print([i**2 for i in [1,2,3] if i==2])
"""
[出力]
[4]
"""

#セット内包表記
print({i**2 for i in [1,2,3]})
"""
[出力]
set([1, 4, 9])
"""

#ディクショナリ内包表記
print({i:i**2 for i in [1,2,3]})
"""
[出力]
{1: 1, 2: 4, 3: 9}
"""
